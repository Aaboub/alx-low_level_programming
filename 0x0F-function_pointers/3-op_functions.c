/**
 * op_add - Entry
 * @a: param
 * @b: param
 *
 * Return: a+b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Entry
 * @a: param
 * @b: param
 *
 * Return: a+b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

